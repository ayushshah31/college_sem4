class Student():

    def setID(self, id):
        self.id = id

    def getID(self):
        return self.id

    def setName(self, name):
        self.name = name

    def getName(self):
        return self.name

    def setMarks(self, marks):
        self.marks = marks

    def getMarks(self):
        return self.marks

    def setAddr(self, addr):
        self.addr = addr

    def getAddr(self):
        return self.addr
